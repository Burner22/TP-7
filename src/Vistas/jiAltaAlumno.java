    private void jbGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGuardarActionPerformed
        
        try{      
            String a = jtNombre.getText();
            String b = jtApellido.getText();
            Integer c = Integer.valueOf(jtLegajo.getText());
            boolean k = false;

            Alumno a1 = new Alumno (c,b,a);
   
            for (Alumno alumno : totAlu) {
                if(alumno.equals(a1)){
                    JOptionPane.showMessageDialog(this, "Su alumno ya existe");
                    k = true;
                }                
            }
            
            if (k == false ){
                totAlu.add(a1);
                JOptionPane.showMessageDialog(this, "Su alumno se ha agregado con exito");                
            }
            
            a1 = null;
            }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Datos incorrectos ingresados");
        }
    }//GEN-LAST:event_jbGuardarActionPerformed


